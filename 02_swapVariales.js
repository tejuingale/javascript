
console.log("--------------------swap two variable------------");
console.log("---------------------------------------------------");
var sweety = "Sweety";
var cutie = "Cutie";
console.log("Beforeswap---" , "Name1:",sweety, "    Name2: ",cutie);
var temp = sweety;
sweety = cutie;
cutie = temp;
console.log("Afterswap---" , "Name1:",sweety, "   Name2: ",cutie);

